package com.rova.g3an.models;

public class Orders {
    private int id;
    private String name;

    public Orders() {
    }

    public Orders(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
